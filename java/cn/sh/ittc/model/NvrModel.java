public class NvrModel {
	private int nvrId;
	private String nvrIp;
	private int nvrPort;
	private String nvrLoginName;
	private String nvrLoginPwd;
	private String brand;
	private String storage;
	/**
	 * @return nvrId
	 */
	public int getNvrId() {
		return nvrId;
	}
	/**
	 * @param nvrId the nvrId to set
	 */
	public void setNvrId(int nvrId) {
		this.nvrId = nvrId;
	}
	/**
	 * @return nvrIp
	 */
	public String getNvrIp() {
		return nvrIp;
	}
	/**
	 * @param nvrIp the nvrIp to set
	 */
	public void setNvrIp(String nvrIp) {
		this.nvrIp = nvrIp;
	}
	/**
	 * @return nvrPort
	 */
	public int getNvrPort() {
		return nvrPort;
	}
	/**
	 * @param nvrPort the nvrPort to set
	 */
	public void setNvrPort(int nvrPort) {
		this.nvrPort = nvrPort;
	}
	/**
	 * @return nvrLoginName
	 */
	public String getNvrLoginName() {
		return nvrLoginName;
	}
	/**
	 * @param nvrLoginName the nvrLoginName to set
	 */
	public void setNvrLoginName(String nvrLoginName) {
		this.nvrLoginName = nvrLoginName;
	}
	/**
	 * @return nvrLoginPwd
	 */
	public String getNvrLoginPwd() {
		return nvrLoginPwd;
	}
	/**
	 * @param nvrLoginPwd the nvrLoginPwd to set
	 */
	public void setNvrLoginPwd(String nvrLoginPwd) {
		this.nvrLoginPwd = nvrLoginPwd;
	}
	/**
	 * @return brand
	 */
	public String getBrand() {
		return brand;
	}
	/**
	 * @param brand the brand to set
	 */
	public void setBrand(String brand) {
		this.brand = brand;
	}
	/**
	 * @return storage
	 */
	public String getStorage() {
		return storage;
	}
	/**
	 * @param storage the storage to set
	 */
	public void setStorage(String storage) {
		this.storage = storage;
	}
	
	

}
